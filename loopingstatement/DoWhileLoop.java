package loopingstatement;

public class DoWhileLoop {

	public static void main(String[] args) {

// 1 to 10 number print
		
		int i =1;
		do {
			System.out.println(i);
			i++;
		} while(i<=10);
	
	
	// 10.... to ....1 decending order
		
		int i1= 10;
		
		do {
			
			System.out.println(i1);
			i1--;
		} while (i1>=0);
	
	}

}
