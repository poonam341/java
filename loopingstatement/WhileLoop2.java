package loopingstatement;

public class WhileLoop2 {

	public static void main(String[] args) {
		// example 2 print Hello 10 times
		
			int i =1;  // initialization
		
		while(i<=10)  // condition 
		{
			System.out.println("Hello");
			i++;            // increment 
		}
		
		

	}

}
