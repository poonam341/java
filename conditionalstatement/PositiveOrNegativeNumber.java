package conditionalstatement;

public class PositiveOrNegativeNumber {

	public static void main(String[] args) {
		
		int num=-10;
		
		if (num>0) {
			System.out.println("Positive Number");
		}
		else if (num<0)
		{
			System.out.println("negative Number");
		}
		else
		{
			System.out.println("zero");
		}
		
	}

}
