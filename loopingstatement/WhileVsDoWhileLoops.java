package loopingstatement;

public class WhileVsDoWhileLoops {

	public static void main(String[] args) {

			// while Loop
		
			int i=1;
			
			while(i<=5)
			{
				System.out.println(i);
				i++;
			}

			// do while loop
			
			int i1=1;
			
			do
			{
				System.out.println(i1);
				i1++;
			}while(i1<=10);
	}

}
