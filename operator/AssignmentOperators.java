package operator;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		// Assignment Operator (+   +=   -=   *=   /=   %=)
		
		int a=10;
		
		a+=5;   		 //a=a+5;
		System.out.println(a); 		//15
		
		a-=6;			//a=a-6
		System.out.println(a);   	// 9
		
		a*=2;			//a=a*2
		System.out.println(a); 		// 18
		
		a/=2; 			//a=a/2
		System.out.println(a); 		//9
		
		a%=2;   		//a=a%2
		System.out.println(a);    //1
		
		
		
		
		

	}

}
